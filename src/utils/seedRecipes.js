/**
 * Seed recipes to Firestore
 * This is a temporary function for development/testing
 */
export const seedRecipes = async () => {
  try {
    console.log('ğŸŒ± Starting to seed recipes to Firestore...')
    console.log(`ğŸ“¦ Total recipes to add: ${recipes.length}`)

    let successCount = 0
    let errorCount = 0

    // Add each recipe to Firestore
    for (const recipe of recipes) {
      try {
        // Use 'id' field as document ID for consistency
        await setDoc(doc(db, 'recipes', recipe.id), recipe)
        console.log(`âœ… Added recipe: ${recipe.title} (ID: ${recipe.id})`)
        successCount++
      } catch (error) {
        console.error(`âŒ Error adding recipe ${recipe.title}:`, error)
        errorCount++
      }
    }

    console.log('\nğŸ“Š Seeding Summary:')
    console.log(`   âœ… Success: ${successCount} recipes`)
    console.log(`   âŒ Errors: ${errorCount} recipes`)
    console.log('\nğŸ‰ Recipe seeding completed!')

    return { success: successCount, errors: errorCount }
  } catch (error) {
    console.error('âŒ Fatal error during seeding:', error)
    throw error
  }
}

